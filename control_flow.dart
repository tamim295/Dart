//conditional statement

void main() {
  int num = 90;
  if (num % 2 == 0) {
    print('even');
  } else if (num % 3 == 0) {
    print('Odd');
  } else {
    print('confused');
  }

  int number = 1;
  switch (number) {
    case 0:
      print('Even');
      break;
    case 1:
      print('Odd');
      break;
    default:
      print('Confused');


    
  }
}
